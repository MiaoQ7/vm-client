def imei_check_code(code):
  total, sum1, sum2, temp = 0, 0, 0, 0
  for i, ch in enumerate(code):
    num = ord(ch)
    if i % 2 == 0:
      sum1 += num
    else:
      temp = num * 2
      if temp < 10:
        sum2 += temp
      else:
        sum2 = sum2 + temp + 1 - 10
  total = sum1 + sum2
  return "0" if total % 10 == 0 else str((10 - (total % 10)))

def calculate_imei(imei_tac, imei_snr):
  sb = str(imei_tac)
  snr = str(imei_snr)
  while len(sb) + len(snr) < 14:
    sb += 0
  sb += snr
  sb += imei_check_code(sb)
  return sb

