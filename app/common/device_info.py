def random_mac(arg10=""):
  v0, v1, v7, v8, v9 = "", 3, 5, 6, 8
  if arg10 != "":
    v3 = arg10 + ":"
    v0_1 = v1
  else:
    v3 = v0
    v0_1 = 0
  v5 = ["0", "2", "4", "6", "8", "a", "c", "e"]
  while v0_1 < v8:
    v1_1 = v3 + hex(random.randrange(0, 16))[2:]
    v3 = v1_1 + v5[random.randrange(0, v9)] if v0_1 == 0 else v1_1 + hex(random.randrange(0, 16))[2:]
    if v0_1 < v7:
      v3 = v3 + ":"
    v0_1 += 1
  return v3.lower()

